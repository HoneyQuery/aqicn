package cn.edu.ruc.aqi;

public class Data {
	private int AQI;
	private double pm25h;
	private double pm25d;
	
	public int getAQI() {
		return AQI;
	}

	public void setAQI(int AQI) {
		this.AQI = AQI;
	}

	public double getPm25h() {
		return pm25h;
	}

	public void setPm25h(double pm25h) {
		this.pm25h = pm25h;
	}

	public double getPm25d() {
		return pm25d;
	}

	public void setPm25d(double pm25d) {
		this.pm25d = pm25d;
	}
}
